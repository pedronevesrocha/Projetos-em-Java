
public class protetorSolar extends Produto {
	private int fatorProtecao;
	private String marca;
	private int preco;
	public int getFatorProtecao() {
		return fatorProtecao;
	}
	public void setFatorProtecao(int fatorProtecao) {
		this.fatorProtecao = fatorProtecao;
	}
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public int getPreco() {
		return preco;
	}
	public void setPreco(int preco) {
		this.preco = preco;
	}
}
