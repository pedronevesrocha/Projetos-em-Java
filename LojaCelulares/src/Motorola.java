
public class Motorola extends Produto {
	private int versaoAndroid;
	private int versaoCelular;
	private int tamanho;
	private int peso;
	
	public int getVersaoAndroid() {
		return versaoAndroid;
	}
	public void setVersaoAndroid(int versaoAndroid) {
		this.versaoAndroid = versaoAndroid;
	}
	public int getVersaoCelular() {
		return versaoCelular;
	}
	public void setVersaoCelular(int versaoCelular) {
		this.versaoCelular = versaoCelular;
	}
	public int getTamanho() {
		return tamanho;
	}
	public void setTamanho(int tamanho) {
		this.tamanho = tamanho;
	}
	public int getPeso() {
		return peso;
	}
	public void setPeso(int peso) {
		this.peso = peso;
	}
}
